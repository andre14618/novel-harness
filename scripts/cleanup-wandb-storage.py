"""Audit and clean up W&B artifact storage for novel-harness.

Dry-run by default — shows what would be kept vs deleted with sizes.
Pass --delete to actually purge.

Usage:
  set -a; source .env; set +a
  python3 scripts/cleanup-wandb-storage.py          # dry run
  python3 scripts/cleanup-wandb-storage.py --delete  # actually delete
"""

import sys
import wandb

ENTITY = "andre14618-"
PROJECT = "novel-harness"

# ── Only keep what W&B Inference needs to SERVE ──────────────────────────
# Everything else (train-state, intermediate checkpoints, datasets, identity
# LoRAs) is deleted. Train-state can be re-generated by resuming training;
# training data lives in lora-data/ locally.
KEEP_SERVING = {
    # adherence-checker agent (models/roles.ts)
    ("adherence-checker-v2-sft-resume", "v9"),
    # adherence-events agent
    ("adherence-checker-v4", None),  # None = keep ALL versions
    # tonal-pass agent
    ("howard-tonal-v4-sft-resume", "v8"),
    # in-eval adapters
    ("chapter-plan-checker-v2", None),
    ("chapter-plan-checker-v2-sft-resume", None),
    ("continuity-v2", None),
    ("continuity-v2-sft-resume", None),
}


def should_keep(collection_name: str, version_name: str) -> tuple[bool, str]:
    """Return (keep, reason) for a specific artifact version."""
    version_tag = version_name.split(":")[-1] if ":" in version_name else None

    for keep_name, keep_ver in KEEP_SERVING:
        if collection_name == keep_name:
            if keep_ver is None:
                return True, "KEEP (serving — all versions)"
            if version_tag == keep_ver:
                return True, f"KEEP (serving: {keep_name}:{keep_ver})"

    return False, "DELETE"


def fmt_bytes(n: int) -> str:
    if n < 1024:
        return f"{n} B"
    if n < 1024 * 1024:
        return f"{n / 1024:.1f} KB"
    if n < 1024 * 1024 * 1024:
        return f"{n / (1024 * 1024):.1f} MB"
    return f"{n / (1024 * 1024 * 1024):.2f} GB"


def main():
    do_delete = "--delete" in sys.argv
    api = wandb.Api()

    total_keep = 0
    total_delete = 0
    delete_targets = []

    print(f"{'=' * 70}")
    print(f"W&B Storage Audit — {ENTITY}/{PROJECT}")
    print(f"Mode: {'DELETE' if do_delete else 'DRY RUN'}")
    print(f"{'=' * 70}\n")

    for atype in api.artifact_types(f"{ENTITY}/{PROJECT}"):
        for coll in atype.collections():
            try:
                versions = list(coll.artifacts())
            except Exception:
                continue

            if not versions:
                continue

            print(f"Collection: {coll.name}  (type: {atype.name})")

            for v in versions:
                aliases = list(v.aliases) if v.aliases else []
                size = v.size or 0

                keep, reason = should_keep(coll.name, v.name)

                marker = "  K" if keep else "  X"
                print(
                    f"{marker} {v.name:55s}  "
                    f"{fmt_bytes(size):>10s}  "
                    f"aliases={aliases}  "
                    f"{reason}"
                )

                if keep:
                    total_keep += size
                else:
                    total_delete += size
                    delete_targets.append(v)

            print()

    # ── Summary ──────────────────────────────────────────────────────────
    print(f"{'=' * 70}")
    print("SUMMARY")
    print(f"{'=' * 70}")
    print(f"  Keep on W&B:     {fmt_bytes(total_keep)}")
    print(f"  Delete from W&B: {fmt_bytes(total_delete)}  ({len(delete_targets)} artifacts)")
    print(f"  Total scanned:   {fmt_bytes(total_keep + total_delete)}")
    print()
    print(f"  After cleanup:   ~{fmt_bytes(total_keep)}")
    headroom = 5 * 1024**3 - total_keep
    print(f"  Free tier left:  ~{fmt_bytes(max(0, headroom))}")
    print()

    if not delete_targets:
        print("Nothing to delete — storage is already minimal.")
        return

    if not do_delete:
        print("Run with --delete to purge the marked artifacts.")
        return

    # ── Execute deletions ────────────────────────────────────────────────
    # W&B requires aliases to be stripped before an artifact can be deleted.
    # ART auto-assigns aliases (latest, step0, step1, sft-resume-*) that
    # block deletion with HTTP 409 if not removed first.
    print(f"Deleting {len(delete_targets)} artifact versions...\n")
    deleted = 0
    failed = 0
    for v in delete_targets:
        try:
            # Strip aliases first (required for ART-managed artifacts)
            if v.aliases:
                v.aliases = []
                v.save()
            print(f"  Deleting {v.name} ...", end=" ", flush=True)
            v.delete()
            print("done")
            deleted += 1
        except Exception as e:
            print(f"FAILED: {e}")
            failed += 1

    print(f"\nDeleted {deleted} artifacts. Failed: {failed}. Freed ~{fmt_bytes(total_delete)}.")
    print(f"W&B storage now ~{fmt_bytes(total_keep)}.")


if __name__ == "__main__":
    main()
