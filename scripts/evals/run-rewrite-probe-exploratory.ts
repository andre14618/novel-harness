#!/usr/bin/env bun

/**
 * run-rewrite-probe-exploratory.ts
 *
 * Quick directional signal: can `salvatore-1988-v4` use critique to fix
 * specific defects in its own prose, or does it just redraft?
 *
 * THIS IS EXPLORATORY, NOT SHIP-GATING. Accepts the isolation confound
 * Codex flagged in charter round-1 RED (arm shapes differ, not just
 * critique). Accepts small N. Accepts hand-built retry shape instead of
 * the production retry-context builder. The goal is a cheap, fast read
 * on whether the rewrite-probe charter is worth investing the ~3-4 hours
 * of rigorous-infra work to pursue.
 *
 * Cost: ~$0.10 writer + ~$0 Sonnet judge. Wall clock ~5 min for writer
 * calls; judging runs as a separate Agent-subagent pass after.
 *
 * No circularity risk: critiques are generated by regex/word-count
 * detectors, not by an LLM. Sonnet is only the judge.
 *
 * Usage:
 *   bun scripts/evals/run-rewrite-probe-exploratory.ts \
 *     --triplets output/evals/conditioning-floor-pilot-v1-triplets.json \
 *     --out output/evals/rewrite-probe-exploratory-pairs.jsonl
 */

import { createHash } from "node:crypto"
import { readFile, writeFile, mkdir } from "node:fs/promises"
import path from "node:path"
import db from "../../src/db/connection"
import { getChapterOutline } from "../../src/db/outlines"
import { getCharacters, getWorldBible } from "../../src/db/world"
import { getCharacterStatesAtChapter } from "../../src/db/character-states"
import { buildBeatContext } from "../../src/agents/writer/beat-context"
import { resolveReferences } from "../../src/agents/writer/reference-resolver"
import { resolveWriterPack } from "../../src/models/roles"
import { executeAndLog } from "../../src/llm"
import { getTokenCost } from "../../src/models/registry"
import type { LLMRequest } from "../../src/transport"

type Triplet = {
  pair_id: string
  pov_character: string
  characters_present: string[]
  beat_description: string
  rotation_prose: string
  words_rotation: number
}

type Defect = { kind: string; description: string; metadata?: Record<string, unknown> }

function parseArgs() {
  const argv = process.argv.slice(2)
  const get = (flag: string) => {
    const i = argv.indexOf(flag)
    return i >= 0 && i + 1 < argv.length ? argv[i + 1] : undefined
  }
  return {
    triplets: get("--triplets") ?? "output/evals/conditioning-floor-pilot-v1-triplets.json",
    out: get("--out") ?? "output/evals/rewrite-probe-exploratory-pairs.jsonl",
    sourceNovelId: get("--source") ?? "pp2-floor__prompt__fantasy-debt__1776710485411",
    pairsPath: get("--pairs") ?? "output/evals/conditioning-floor-pairs-v1.jsonl",
  }
}

/** Simple regex-based repetition detector: find trigrams that appear ≥3 times. */
function detectRepetition(prose: string): Defect[] {
  const tokens = prose.toLowerCase().replace(/[.!?,;:"']/g, " ").split(/\s+/).filter(Boolean)
  const counts = new Map<string, number>()
  for (let i = 0; i + 2 < tokens.length; i++) {
    const key = `${tokens[i]} ${tokens[i+1]} ${tokens[i+2]}`
    counts.set(key, (counts.get(key) ?? 0) + 1)
  }
  const hits: Defect[] = []
  for (const [trigram, n] of counts) {
    if (n >= 3 && !/^(the|and|of|to|a|in|for|on|with|was|is) /.test(trigram)) {
      hits.push({
        kind: "repetition",
        description: `The phrase "${trigram}" appears ${n} times — rewrite to vary.`,
        metadata: { trigram, count: n },
      })
    }
  }
  return hits.slice(0, 3) // top 3 most egregious
}

/** Underlength detector. */
function detectUnderlength(prose: string, minWords = 100): Defect[] {
  const words = prose.trim().split(/\s+/).filter(Boolean).length
  if (words < minWords) {
    return [{
      kind: "underlength",
      description: `The prose is ${words} words — expand to at least ${minWords} while keeping character voices distinct.`,
      metadata: { words, minWords },
    }]
  }
  return []
}

/** Render defects into a single critique string. */
function renderCritique(defects: Defect[]): string {
  if (defects.length === 0) {
    return "Sharpen the distinction between each speaking character's voice in diction, cadence, and register."
  }
  return defects.map(d => `- ${d.description}`).join("\n")
}

async function main() {
  const args = parseArgs()
  console.log(`[probe] loading triplets from ${args.triplets}`)
  const wrap = JSON.parse(await readFile(args.triplets, "utf8"))
  const triplets: Triplet[] = wrap.triplets ?? wrap
  console.log(`[probe] ${triplets.length} triplets loaded`)

  // Load pair-entry metadata so we have chapter/beat_index coords for each pair
  const pairsRaw = await readFile(args.pairsPath, "utf8")
  const pairsByPairId = new Map<string, { chapter_number: number; beat_index_in_chapter: number; pov_character: string; characters_present: string[]; description: string }>()
  for (const line of pairsRaw.split("\n").filter(Boolean)) {
    const row = JSON.parse(line)
    const pairId = `${row.novel_id_source}-ch${row.chapter_number}-b${row.beat_index_in_chapter}`
    pairsByPairId.set(pairId, {
      chapter_number: row.chapter_number,
      beat_index_in_chapter: row.beat_index_in_chapter,
      pov_character: row.pov_character,
      characters_present: row.characters_present,
      description: row.description,
    })
  }

  // Set up writer pack
  const novelRow = await db<Array<{ seed_json: Record<string, unknown> }>>`
    SELECT seed_json FROM novels WHERE id = ${args.sourceNovelId}
  `
  if (novelRow.length === 0) {
    console.error(`source novel ${args.sourceNovelId} not found`)
    process.exit(1)
  }
  const genre = (novelRow[0].seed_json as { genre?: string }).genre ?? "fantasy"
  const pack = resolveWriterPack(genre)
  if (!pack) {
    console.error("no writer pack for genre", genre)
    process.exit(1)
  }
  const systemPrompt = await readFile(
    path.resolve(path.dirname(new URL(import.meta.url).pathname), "../../src/agents/writer", pack.systemPromptFile),
    "utf8",
  )

  // Deterministically switch WRITER_CONDITIONING off (raw production behavior).
  // Both arms use raw — the only intended difference is critique vs no critique
  // (with hand-built retry shape accepted as a known caveat).
  delete process.env.WRITER_CONDITIONING

  const pairs: Array<Record<string, unknown>> = []
  let totalCost = 0

  for (const t of triplets) {
    const meta = pairsByPairId.get(t.pair_id)
    if (!meta) {
      console.warn(`[skip] no pair metadata for ${t.pair_id}`)
      continue
    }

    console.log(`\n[beat] ${t.pair_id.split("__").pop()} (ch${meta.chapter_number} b${meta.beat_index_in_chapter}) — V1 rotation prose=${t.words_rotation}w`)

    // 1. Generate critique from regex detectors (no LLM → no circularity)
    const defects: Defect[] = [
      ...detectRepetition(t.rotation_prose),
      ...detectUnderlength(t.rotation_prose),
    ]
    const critique = renderCritique(defects)
    console.log(`  defects: ${defects.length > 0 ? defects.map(d => d.kind).join(",") : "(none — generic critique)"}`)

    // 2. Build shared BeatContext (reused across both arms — as close as we
    //    can get to isolation without the full rigorous infra).
    const outline = await getChapterOutline(args.sourceNovelId, meta.chapter_number)
    const characters = await getCharacters(args.sourceNovelId)
    const characterStates = await getCharacterStatesAtChapter(args.sourceNovelId, meta.chapter_number)
    const worldBible = await getWorldBible(args.sourceNovelId)
    const beatSpec = outline.scenes[meta.beat_index_in_chapter]
    if (!beatSpec) {
      console.warn(`  [skip] beat out of range`)
      continue
    }
    const preResolvedRefs = await resolveReferences(beatSpec, outline, args.sourceNovelId, meta.chapter_number, characters)

    // prior-beat prose: we don't need byte parity for this probe, so pull the
    // earliest successful prior-beat prose (matches what the conditioning-floor
    // replay did).
    let previousBeatProse: string | undefined
    if (meta.beat_index_in_chapter > 0) {
      const priorRows = await db<Array<{ response_content: string }>>`
        SELECT response_content FROM llm_calls
        WHERE novel_id = ${args.sourceNovelId}
          AND agent = 'beat-writer'
          AND chapter = ${meta.chapter_number}
          AND beat_index = ${meta.beat_index_in_chapter - 1}
          AND failed IS NOT TRUE AND response_content IS NOT NULL
        ORDER BY id ASC LIMIT 1
      `
      if (priorRows.length > 0) {
        const sentences = priorRows[0].response_content.split(/(?<=[.!?])\s+/).filter(s => s.trim())
        previousBeatProse = sentences.slice(-3).join(" ")
      }
    }

    const beatCtx = await buildBeatContext({
      novelId: args.sourceNovelId,
      chapterNumber: meta.chapter_number,
      beatIndex: meta.beat_index_in_chapter,
      previousBeatProse,
      outline,
      characters,
      characterStates,
      worldBible,
      preResolvedRefs,
      compactMode: true,
      genre,
    })

    // 3. Arm (a) redraft — fresh call with just the BeatContext, no V1 prose.
    const requestA: LLMRequest = {
      systemPrompt,
      userPrompt: beatCtx.userPrompt,
      model: pack.model.model,
      provider: pack.model.provider,
      temperature: pack.model.temperature ?? 0.8,
      maxTokens: pack.model.maxTokens ?? 4000,
      responseFormat: { type: "text" },
      noRetries: true,
    }

    // 4. Arm (b) rewrite — BeatContext + V1 prose + critique, hand-built shape.
    //    KNOWN CAVEAT: this differs from production retry-context builder.
    //    Accept for exploratory directional signal only.
    const armBUserPrompt = [
      beatCtx.userPrompt,
      "",
      "---",
      "V1 DRAFT (your prior attempt at this beat — rewrite to address the issues below):",
      t.rotation_prose,
      "",
      "ISSUES TO FIX:",
      critique,
      "",
      "Rewrite the beat now, preserving voice and plot but addressing the issues above.",
    ].join("\n")
    const requestB: LLMRequest = {
      systemPrompt,
      userPrompt: armBUserPrompt,
      model: pack.model.model,
      provider: pack.model.provider,
      temperature: pack.model.temperature ?? 0.8,
      maxTokens: pack.model.maxTokens ?? 4000,
      responseFormat: { type: "text" },
      noRetries: true,
    }

    let armAProse = "", armBProse = "", errorA: string | undefined, errorB: string | undefined
    try {
      console.log("  [A] redraft…")
      const ra = await executeAndLog(requestA, undefined, "rewrite-probe-exploratory-redraft", { chapter: meta.chapter_number, beatIndex: meta.beat_index_in_chapter, attempt: 1 }, { meta: { probe: "arm-a-redraft", pair_id: t.pair_id } })
      armAProse = ra.content
      totalCost += getTokenCost(pack.model.provider, pack.model.model, ra.usage.prompt_tokens, ra.usage.completion_tokens, ra.usage.cached_tokens)
    } catch (e) { errorA = e instanceof Error ? e.message : String(e); console.warn("  [A] FAIL:", errorA.slice(0, 120)) }

    try {
      console.log("  [B] rewrite with critique…")
      const rb = await executeAndLog(requestB, undefined, "rewrite-probe-exploratory-rewrite", { chapter: meta.chapter_number, beatIndex: meta.beat_index_in_chapter, attempt: 1 }, { meta: { probe: "arm-b-rewrite", pair_id: t.pair_id, critique } })
      armBProse = rb.content
      totalCost += getTokenCost(pack.model.provider, pack.model.model, rb.usage.prompt_tokens, rb.usage.completion_tokens, rb.usage.cached_tokens)
    } catch (e) { errorB = e instanceof Error ? e.message : String(e); console.warn("  [B] FAIL:", errorB.slice(0, 120)) }

    // Shuffle A/B deterministically per pair_id via sha256
    const seed = "rewrite-probe-exploratory-v1"
    const digest = createHash("sha256").update(`${seed}:${t.pair_id}`).digest()
    const swap = digest.readUInt32BE(0) % 2 === 0
    const proseA = swap ? armBProse : armAProse
    const proseB = swap ? armAProse : armBProse
    const labelA = swap ? "rewrite" : "redraft"
    const labelB = swap ? "redraft" : "rewrite"

    const armAWords = armAProse.trim().split(/\s+/).filter(Boolean).length
    const armBWords = armBProse.trim().split(/\s+/).filter(Boolean).length

    pairs.push({
      pair_id: t.pair_id,
      pov_character: t.pov_character,
      characters_present: t.characters_present,
      beat_description: t.beat_description,
      v1_rotation_prose: t.rotation_prose,
      v1_rotation_words: t.words_rotation,
      critique,
      defects: defects.map(d => d.kind),
      arm_a_prose: proseA,
      arm_b_prose: proseB,
      arm_a_label: labelA,
      arm_b_label: labelB,
      words_redraft: armAWords,
      words_rewrite: armBWords,
      error_redraft: errorA,
      error_rewrite: errorB,
    })

    console.log(`  [OK] redraft=${armAWords}w rewrite=${armBWords}w, running cost=$${totalCost.toFixed(5)}`)
  }

  await mkdir(path.dirname(path.resolve(args.out)), { recursive: true })
  await writeFile(path.resolve(args.out), pairs.map(p => JSON.stringify(p)).join("\n") + "\n", "utf8")
  console.log(`\n[probe] wrote ${pairs.length} pairs to ${args.out}`)
  console.log(`[probe] total writer cost: $${totalCost.toFixed(5)}`)
  console.log(`[probe] next: pipe --pairs into a Sonnet-subagent judge pass (not codex exec per memory).`)
}

if (import.meta.main) {
  main().catch(e => {
    console.error(e instanceof Error ? e.stack ?? e.message : String(e))
    process.exit(1)
  })
}
